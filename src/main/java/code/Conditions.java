package code;

public class Conditions {
    public static void main(String[] args) {
        int x = 6;
        int y = 6;

        if (x > y){
            System.out.println("x > y");
        } else if (x == y) {
            System.out.println("x = y");
        } else {
            System.out.println("x < y");
        }
    }
}
