package amigos;

public class Math {
    public static void main(String[] args) {
        System.out.println(java.lang.Math.abs(-10));
        System.out.println(java.lang.Math.max(6, -10));
        System.out.println(java.lang.Math.min(6, -10));
        System.out.println((int) java.lang.Math.pow(5,3));
        System.out.println((int) java.lang.Math.sqrt(49));
        System.out.println(java.lang.Math.PI);
    }
}
